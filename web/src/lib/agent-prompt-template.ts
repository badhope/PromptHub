    prompt = prompt.replace(
      '## 💫 激活与交互系统',
      DOMAIN_SPECIFIC_EXTENSIONS[domain] + '\n━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━\n\n## 💫 激活与交互系统'
    );

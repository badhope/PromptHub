  const worldMap = {
    'Dragon Ball': '龙珠', 'Naruto': '火影忍者', 'Bleach': '死神',
    'Attack on Titan': '进击的巨人', 'Demon Slayer': '鬼灭之刃',
    'Jujutsu Kaisen': '咒术回战', 'Chainsaw Man': '电锯人',
    'Spy x Family': '间谍过家家', 'Genshin Impact': '原神',
    'Fate': 'Fate系列', 'Death Note': '死亡笔记',
    'Sword Art Online': '刀剑神域'
  };
  
  skillsData.skills = skillsData.skills.map(skill => {
    let activation = FINAL_ACTIVATIONS[skill.id];
    
    if (!activation) {
      let world = '二次元';
      for (const [key, value] of Object.entries(worldMap)) {
        if (skill.name.includes(key)) {
          world = value;
          break;
        }
      }
      activation = CHARACTER_PROMPT(skill.name, world);
    }
    
    console.log(`✅ ${skill.name}`);
    
    skill.activation_command = {
      content_markdown: activation,
      version: 'FINAL'
    };
    
    updatedCount++;
    return skill;
  });
  
  fs.writeFileSync(SKILLS_JSON_PATH, JSON.stringify(skillsData, null, 2), 'utf8');
  

let updatedCount = 0;

data.skills = data.skills.map(skill => {
  const allDescs = [
    ...descriptions.characters,
    ...descriptions.professional,
    ...descriptions.creative,
    ...descriptions.functional,
  ];
  
  const match = allDescs.find(d => matchSkill(skill.name, d.keys) || matchSkill(skill.id, d.keys));
  
  if (match) {
    updatedCount++;
    return {
      ...skill,
      metadata: {
        ...skill.metadata,
        description: match.desc
      }
    };
  }
  
  if (!skill.metadata.description || skill.metadata.description.length < 20) {
    updatedCount++;
    const defaultDesc = `${skill.name}，一个实用又有趣的AI技能。可以陪你聊天，给你专业的帮助和建议，是你工作生活的好帮手。`;
    return {
      ...skill,
      metadata: {
        ...skill.metadata,
        description: defaultDesc
      }
    };
  }
  
  return skill;
});

